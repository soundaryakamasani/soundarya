package org.casio.calculatorapp;

public class Addition {
	int add(int num1,int num2){
		int result=num1+num2;
		return result;
	}
int add(int num3,int num4,int num5){
	int res=num3+num4+num5;
	return res;
}
int add(int num6,int num7,int num8,int num9){
	int total=num6+num7+num8+num9;
	return total;
	
}
}
